import React, { useState } from "react";
import Link from "next/link";
import { useAtom } from "jotai";
import { userEmailAtom } from "components/state";
import Router from "next/router";
import Auth from "layouts/Auth.js";

export default function Login() {
  const [email, setEmail] = useAtom(userEmailAtom);
  const [password, setPassword] = useState("");
  const [errorMessage, setErrorMessage] = useState("");

  const handleSubmit = (e) => {
    e.preventDefault();
    setErrorMessage("");

    const requestUrl = `http://localhost:3010/login?email=${email}&password=${password}`;
    fetch(requestUrl)
      .then((response) => {
        response.json().then((data) => {
          if (data.access_level === "RO") {
            // Redirect to RO Dashboard
            Router.push({
              pathname: window.location.origin + "/ro/dashboard",
              query: { email: email },
            });
          } else if (data.access_level === "Instructor") {
            // Redirect to Instructor Dashboard
            Router.push({
              pathname: window.location.origin + "/instructor/dashboard",
              query: { email: email },
            });
          } else if (data.access_level === "Student") {
            // Redirect to Student Dashboard
            Router.push({
              pathname: window.location.origin + "/student/dashboard",
              query: { email: email },
            });
          } else if (data.access_level === "None") {
            // Incorrect User name, it is neither admin, instructor or student
            const msg =
              "Incorrect User name, it is neither admin, instructor or student";
            console.log(msg);
            setErrorMessage(msg);
          } else {
            // Unknown Error
            const msg = "Unknown Error";
            console.log(msg);
            setErrorMessage(msg);
          }
        });
      })
      .catch((err) => {
        console.log(err.message);
      });
  };

  return (
    <>
      <div className="container mx-auto px-4 h-full">
        <div className="flex content-center items-center justify-center h-full">
          <div className="w-full lg:w-4/12 px-4">
            <div className="relative flex flex-col min-w-0 break-words w-full mb-6 shadow-lg rounded-lg bg-blueGray-200 border-0">
              <div className="rounded-t mb-0 px-6 py-6">
                <div className="text-center mb-3">
                  <h6 className="text-blueGray-600 text-sm font-bold">
                    Sign in with credentials
                  </h6>
                </div>
                {/* <div className="btn-wrapper text-center">
                  <button
                    className="bg-white active:bg-blueGray-50 text-blueGray-700 font-normal px-4 py-2 rounded outline-none focus:outline-none mr-2 mb-1 uppercase shadow hover:shadow-md inline-flex items-center font-bold text-xs ease-linear transition-all duration-150"
                    type="button"
                  >
                    <img alt="..." className="w-5 mr-1" src="/img/github.svg" />
                    Github
                  </button>
                  <button
                    className="bg-white active:bg-blueGray-50 text-blueGray-700 font-normal px-4 py-2 rounded outline-none focus:outline-none mr-1 mb-1 uppercase shadow hover:shadow-md inline-flex items-center font-bold text-xs ease-linear transition-all duration-150"
                    type="button"
                  >
                    <img alt="..." className="w-5 mr-1" src="/img/google.svg" />
                    Google
                  </button>
                </div> */}
                <hr className="mt-6 border-b-1 border-blueGray-300" />
              </div>
              <div className="flex-auto px-4 lg:px-10 py-10 pt-0">
                {/* <div className="text-blueGray-400 text-center mb-3 font-bold">
                  <small>sign in with credentials</small>
                </div> */}
                <form>
                  <div className="relative w-full mb-3">
                    <label
                      className="block uppercase text-blueGray-600 text-xs font-bold mb-2"
                      htmlFor="grid-password"
                    >
                      Email
                    </label>
                    <input
                      type="email"
                      className="border-0 px-3 py-3 placeholder-blueGray-300 text-blueGray-600 bg-white rounded text-sm shadow focus:outline-none focus:ring w-full ease-linear transition-all duration-150"
                      placeholder="Email"
                      value={email}
                      onChange={(e) => {
                        setEmail(e.target.value);
                      }}
                    />
                  </div>

                  <div className="relative w-full mb-3">
                    <label
                      className="block uppercase text-blueGray-600 text-xs font-bold mb-2"
                      htmlFor="grid-password"
                    >
                      Password
                    </label>
                    <input
                      type="password"
                      className="border-0 px-3 py-3 placeholder-blueGray-300 text-blueGray-600 bg-white rounded text-sm shadow focus:outline-none focus:ring w-full ease-linear transition-all duration-150"
                      placeholder="Password"
                      value={password}
                      onChange={(e) => {
                        setPassword(e.target.value);
                      }}
                    />
                  </div>

                  <p>{errorMessage}</p>

                  <div className="text-center mt-6">
                    <button
                      className="bg-blueGray-800 text-white active:bg-blueGray-600 text-sm font-bold uppercase px-6 py-3 rounded shadow hover:shadow-lg outline-none focus:outline-none mr-1 mb-1 w-full ease-linear transition-all duration-150"
                      type="submit"
                      onClick={(e) => {
                        handleSubmit(e);
                      }}
                    >
                      Sign In
                    </button>
                  </div>
                </form>
              </div>
            </div>
          </div>
        </div>
      </div>
    </>
  );
}

Login.layout = Auth;
